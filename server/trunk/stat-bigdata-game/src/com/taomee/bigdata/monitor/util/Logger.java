package com.taomee.bigdata.monitor.util;

public class Logger {
    private String stid;
    private String sstid;
    private double value;

    public Logger(String stid, String sstid, double value) {
        this.stid = stid;
        this.sstid = sstid;
        this.value = value;
    }

    public String getStid() {
        return stid;
    }

    public String getSstid() {
        return sstid;
    }

    public double getValue() {
        return value;
    }
}

