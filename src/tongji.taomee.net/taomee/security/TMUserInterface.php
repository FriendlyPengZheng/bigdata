<?php
interface TMUserInterface
{
    /**
     * @brief getUserId 
     * 获取用户唯一ID
     */
    function getUserId();

    /**
     * @brief setUserId 
     * 设置用户唯一ID
     *
     * @param {interval} $userId
     */
    function setUserId($userId);

    /**
     * @brief getUserName
     * 获取用户名称
     */
    function getUserName();

    /**
     * @brief setUserName
     * 设置用户名称
     *
     * @param {string} $userName
     */
    function setUserName($userName);
}
