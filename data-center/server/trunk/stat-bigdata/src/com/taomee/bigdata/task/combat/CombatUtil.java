package com.taomee.bigdata.task.combat;

/**
 * 工具类
 * 
 * @author looper
 * @date 2016年7月6日
 */
public class CombatUtil {
	/**
	 * 判读一个字符串是否是整数
	 * @param str
	 * @return
	 */
	public static boolean isNumeric(String str) {
		for (int i = 0; i < str.length(); i++) {
			//System.out.println(str.charAt(i));
			if (!Character.isDigit(str.charAt(i))) {
				return false;
			}
		}
		return true;
	}

}
