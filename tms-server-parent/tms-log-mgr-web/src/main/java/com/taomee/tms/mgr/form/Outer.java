package com.taomee.tms.mgr.form;

import java.util.List;

public class Outer{
	private Integer outerAttrInteger;
	private String outerAttrString;
	private Inner outerAttrInner;
	private List<Inner> outerAttrInnerList;
	
	public Inner getOuterAttrInner() {
		return outerAttrInner;
	}
	public void setOuterAttrInner(Inner outerAttrInner) {
		this.outerAttrInner = outerAttrInner;
	}
	public Outer(){}
	public Outer(Integer i, String s) {
		outerAttrInteger = i;
		outerAttrString = s;
	}
	public Integer getOuterAttrInteger() {
		return outerAttrInteger;
	}
	public void setOuterAttrInteger(Integer outerAttrInteger) {
		this.outerAttrInteger = outerAttrInteger;
	}
	public String getOuterAttrString() {
		return outerAttrString;
	}
	public void setOuterAttrString(String outerAttrString) {
		this.outerAttrString = outerAttrString;
	}
	public List<Inner> getOuterAttrInnerList() {
		return outerAttrInnerList;
	}
	public void setOuterAttrInnerList(List<Inner> outerAttrInnerList) {
		this.outerAttrInnerList = outerAttrInnerList;
	}
}