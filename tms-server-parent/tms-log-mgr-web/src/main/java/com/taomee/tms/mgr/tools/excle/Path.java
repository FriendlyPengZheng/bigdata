package com.taomee.tms.mgr.tools.excle;

public class Path {
	private Point start;
	private Point end;
	
	public Path(Point point, Point point2) {
		start = point;
		end = point2;
	}

	public Point getSart() {
		return start;
	}
	
	public void setSart(Point start) {
		this.start = start;
	}

	public Point getEnd() {
		return end;
	}

	public void setEnd(Point end) {
		this.end = end;
	}
}
