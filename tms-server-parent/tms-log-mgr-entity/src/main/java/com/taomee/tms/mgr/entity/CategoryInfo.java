package com.taomee.tms.mgr.entity;

import java.io.Serializable;

public class CategoryInfo implements Serializable{

	private Integer category_id;
	private String category_name;
	private Integer is_leaf;
	private String sstid;
	private String time;
	private Integer buycount;
	private Integer buyucount;
	private Integer salenum;
	private Integer salemoney;
	
	public CategoryInfo() {
	}

	public CategoryInfo(Integer category_id, String category_name,
			Integer is_leaf, String sstid, String time, Integer buycount,
			Integer buyucount, Integer salenum, Integer salemoney) {
		this.category_id = category_id;
		this.category_name = category_name;
		this.is_leaf = is_leaf;
		this.sstid = sstid;
		this.time = time;
		this.buycount = buycount;
		this.buyucount = buyucount;
		this.salenum = salenum;
		this.salemoney = salemoney;
	}

	public String getSstid() {
		return sstid;
	}

	public void setSstid(String sstid) {
		this.sstid = sstid;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public Integer getBuycount() {
		return buycount;
	}

	public void setBuycount(Integer buycount) {
		this.buycount = buycount;
	}

	public Integer getBuyucount() {
		return buyucount;
	}

	public void setBuyucount(Integer buyucount) {
		this.buyucount = buyucount;
	}

	public Integer getSalenum() {
		return salenum;
	}

	public void setSalenum(Integer salenum) {
		this.salenum = salenum;
	}

	public Integer getSalemoney() {
		return salemoney;
	}

	public void setSalemoney(Integer salemoney) {
		this.salemoney = salemoney;
	}

	public Integer getCategory_id() {
		return category_id;
	}

	public void setCategory_id(Integer category_id) {
		this.category_id = category_id;
	}

	public String getCategory_name() {
		return category_name;
	}

	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}

	public Integer getIs_leaf() {
		return is_leaf;
	}

	public void setIs_leaf(Integer is_leaf) {
		this.is_leaf = is_leaf;
	}

	
	
}
