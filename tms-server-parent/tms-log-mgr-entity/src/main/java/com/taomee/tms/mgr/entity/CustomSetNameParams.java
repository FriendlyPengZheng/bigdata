package com.taomee.tms.mgr.entity;

import java.io.Serializable;


public class CustomSetNameParams implements Serializable{

	@Override
	public String toString() {
		return "CustomSetNameParams [gameId=" + gameId + ", id=" + id
				+ ", name=" + name + ", type=" + type + "]";
	}
	
	private static final long serialVersionUID = -5376616818795631051L;
	private Integer gameId;
	private Integer id; //schemaId nodeId
	private String name;
	private String type;
	
	public Integer getGameId() {
		return gameId;
	}
	public void setGameId(Integer gameId) {
		this.gameId = gameId;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
	
}
