package com.taomee.tms.mgr.entity;

import java.io.Serializable;
import java.sql.Timestamp;

public class SchemaInfo implements Serializable {
	

	@Override
	public String toString() {
		return "SchemaInfo [schemaId=" + schemaId + ", logId=" + logId
				+ ", op=" + op + ", cascadeFields=" + cascadeFields
				+ ", materialId=" + materialId + ", materialName="
				+ materialName + ", nodeId=" + nodeId + ", status=" + status
				+ ", displayOrder=" + displayOrder + ", schemaName="
				+ schemaName + ", flag=" + flag + ", addTime=" + addTime + "]";
	}

	private static final long serialVersionUID = -7641829234889181361L;

	private Integer schemaId;
	private Integer logId;
	private String op;
	private String cascadeFields;
	private Integer materialId;
	private String materialName;
	private Integer nodeId;
	private Integer status;
	private Integer displayOrder;
	private String schemaName;
	private Integer flag;  
	private Timestamp addTime;
	

	public Integer getSchemaId() {
		return schemaId;
	}

	public void setSchemaId(Integer schemaId) {
		this.schemaId = schemaId;
	}

	public Integer getLogId() {
		return logId;
	}

	public void setLogId(Integer logId) {
		this.logId = logId;
	}

	public String getOp() {
		return op;
	}

	public void setOp(String op) {
		this.op = op;
	}

	public String getCascadeFields() {
		return cascadeFields;
	}

	public void setCascadeFields(String cascadeFields) {
		this.cascadeFields = cascadeFields;
	}

	public Integer getMaterialId() {
		return materialId;
	}

	public void setMaterialId(Integer materialId) {
		this.materialId = materialId;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Timestamp getAddTime() {
		return addTime;
	}

	public void setAddTime(Timestamp addTime) {
		this.addTime = addTime;
	}

	public String getMaterialName() {
		return materialName;
	}

	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}

	public Integer getNodeId() {
		return nodeId;
	}

	public void setNodeId(Integer nodeId) {
		this.nodeId = nodeId;
	}

	public Integer getDisplayOrder() {
		return displayOrder;
	}

	public void setDisplayOrder(Integer displayOrder) {
		this.displayOrder = displayOrder;
	}

	public String getSchemaName() {
		return schemaName;
	}

	public void setSchemaName(String schemaName) {
		this.schemaName = schemaName;
	}

	public Integer getFlag() {
		return flag;
	}

	public void setFlag(Integer flag) {
		this.flag = flag;
	}

}
