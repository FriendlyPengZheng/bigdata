package com.taomee.tms.mgr.entity;

import java.io.Serializable;

public class ZSInfo implements Serializable{
	@Override
	public String toString() {
		return "ZSInfo [zoneServerId=" + zoneServerId + ", zoneServerName="
				+ zoneServerName + "]";
	}

	private static final long serialVersionUID = 1113135770954774568L;

	//zoneServerId用_分隔，例如1区全服(1_-1), 1区1服(1_1)
	private String zoneServerId;
	private String zoneServerName;
	
	public String getZoneServerId() {
		return zoneServerId;
	}
	
	public void setZoneServerId(String zoneServerId) {
		this.zoneServerId = zoneServerId;
	}

	public String getZoneServerName() {
		return zoneServerName;
	}

	public void setZoneServerName(String zoneServerName) {
		this.zoneServerName = zoneServerName;
	}
	
	
}
