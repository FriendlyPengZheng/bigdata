package com.taomee.tms.mgr.entity;

import java.io.Serializable;

public class DataInfoQueryParams implements Serializable {
	
	private static final long serialVersionUID = 8031458257786583063L;

	public DataInfoQueryParams(int type, int relateId) {
		super();
		this.type = type;
		this.relateId = relateId;
	}
	public DataInfoQueryParams() {}
	private int type;
	private int relateId;
	
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getRelateId() {
		return relateId;
	}
	public void setRelateId(int relateId) {
		this.relateId = relateId;
	}
}
