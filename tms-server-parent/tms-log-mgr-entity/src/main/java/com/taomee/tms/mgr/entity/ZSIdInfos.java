package com.taomee.tms.mgr.entity;

import java.io.Serializable;

public class ZSIdInfos implements Serializable{
	private static final long serialVersionUID = 5480214205748523549L;
	private Integer zoneId;
	private Integer sId;
	private String zoneServerName;
	
	public Integer getZoneId() {
		return zoneId;
	}
	public void setZoneId(Integer zoneId) {
		this.zoneId = zoneId;
	}
	public Integer getsId() {
		return sId;
	}
	public void setsId(Integer sId) {
		this.sId = sId;
	}
	public String getZoneServerName() {
		return zoneServerName;
	}
	public void setZoneServerName(String zoneServerName) {
		this.zoneServerName = zoneServerName;
	}

}
