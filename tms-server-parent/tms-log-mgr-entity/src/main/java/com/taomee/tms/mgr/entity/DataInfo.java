package com.taomee.tms.mgr.entity;

import java.io.Serializable;
import java.sql.Timestamp;

public class DataInfo implements Serializable {
	@Override
	public String toString() {
		return "DataInfo [dataId=" + dataId + ", type=" + type + ", relateId="
				+ relateId + ", isLeaf=" + isLeaf + ", parentId=" + parentId
				+ ", dataName=" + dataName + ", sthash=" + sthash
				+ ", addTime=" + addTime + "]";
	}

	private static final long serialVersionUID = 5846893804950979050L;

	private Integer dataId;
	private Integer type;
	private Integer relateId;
	private Integer isLeaf;
	private Integer parentId;
	private String dataName;
	private Integer sthash;
	private Timestamp addTime;

	public Integer getDataId() {
		return dataId;
	}

	public void setDataId(Integer dataId) {
		this.dataId = dataId;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getRelateId() {
		return relateId;
	}

	public void setRelateId(Integer relateId) {
		this.relateId = relateId;
	}

	public Integer getIsLeaf() {
		return isLeaf;
	}

	public void setIsLeaf(Integer isLeaf) {
		this.isLeaf = isLeaf;
	}

	public Integer getParentId() {
		return parentId;
	}

	public void setParentId(Integer parentId) {
		this.parentId = parentId;
	}

	public String getDataName() {
		return dataName;
	}

	public void setDataName(String dataName) {
		this.dataName = dataName;
	}

	public Integer getSthash() {
		return sthash;
	}

	public void setSthash(Integer sthash) {
		this.sthash = sthash;
	}

	public Timestamp getAddTime() {
		return addTime;
	}

	public void setAddTime(Timestamp addTime) {
		this.addTime = addTime;
	}
}
