package com.taomee.tms.mgr.entity;

import java.io.Serializable;
import java.sql.Timestamp;

public class ServerGPZSInfo implements Serializable {
	@Override
	public String toString() {
		return "serverGPZSInfo [serverId=" + serverId + ", gameId=" + gameId
				+ ", platformId=" + platformId + ", zoneId=" + zoneId
				+ ", sId=" + sId + ", status=" + status + ", addTime="
				+ addTime + "]";
	}
	private static final long serialVersionUID = -4557568181958901807L;
	private Integer serverId;
	private Integer gameId;
	private Integer platformId;
	private Integer zoneId;
	private Integer sId;
	private Integer status;
	private Timestamp addTime;
	
	public Integer getServerId() {
		return serverId;
	}
	public void setServerId(Integer serverId) {
		this.serverId = serverId;
	}
	public Integer getGameId() {
		return gameId;
	}
	public void setGameId(Integer gameId) {
		this.gameId = gameId;
	}
	public Integer getPlatformId() {
		return platformId;
	}
	public void setPlatformId(Integer platformId) {
		this.platformId = platformId;
	}
	public Integer getZoneId() {
		return zoneId;
	}
	public void setZoneId(Integer zoneId) {
		this.zoneId = zoneId;
	}
	public Integer getsId() {
		return sId;
	}
	public void setsId(Integer sId) {
		this.sId = sId;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Timestamp getAddTime() {
		return addTime;
	}
	public void setAddTime(Timestamp addTime) {
		this.addTime = addTime;
	}
	
}
