package com.taomee.tms.mgr.entity;

import java.io.Serializable;

public class DeleteDataInfoParams  implements Serializable{

	private static final long serialVersionUID = -8827397614122239385L;

	private Integer relateId;
	private Integer type;
	private String dataName;
	
	public DeleteDataInfoParams(Integer relateId, Integer type, String dataName) {
		super();
		this.relateId = relateId;
		this.type = type;
		this.dataName = dataName;
	}
	
	public String getDataName() {
		return dataName;
	}
	public void setDataName(String dataName) {
		this.dataName = dataName;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getRelateId() {
		return relateId;
	}
	public void setRelateId(Integer relateId) {
		this.relateId = relateId;
	}
	
	@Override
	public String toString() {
		return "DeleteDataInfoParams [dataName=" + dataName + ", type=" + type
				+ ", relateId=" + relateId + "]";
	}

}
