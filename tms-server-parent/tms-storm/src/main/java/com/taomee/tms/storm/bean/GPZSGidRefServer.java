public class GPZSGidRefServer {

    //gid,pid,zid,sid映射之前的gpzs信息
    private Integer gid;
    private Integer pid;
    private Integer zid;
    private Integer sid;

    private Integer serverId;//server 信息

    public GPZSGidRefServer() {
    }

    public GPZSGidRefServer(Integer gid, Integer pid, Integer zid, Integer sid, Integer serverId) {

        this.gid = gid;
        this.pid = pid;
        this.zid = zid;
        this.sid = sid;
        this.serverId = serverId;
    }

    public Integer getGid() {

        return gid;
    }

    public void setGid(Integer gid) {
        this.gid = gid;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }

    public Integer getZid() {
        return zid;
    }

    public void setZid(Integer zid) {
        this.zid = zid;
    }

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public Integer getServerId() {
        return serverId;
    }

    public void setServerId(Integer serverId) {
        this.serverId = serverId;
    }
}
